    private void bttnVerdadeiroActionPerformed(java.awt.event.ActionEvent evt) {  
    	numeroQuestao++;
    	tela.jLabel3.setText(String.valueOf(numeroQuestao));
       int condicao = 1;
       String idFlashCard = this.idFlashCard;
       boolean acertou = new FlashCardQuestaoController().verificarResposta(condicao, idFlashCard, acertos, numeroQuestao, tela);
       if(acertou) {
			acertos++;
			 tela.lblPontuacao.setText(String.valueOf(acertos));
		}
       new FlashCardQuestaoDAO().visualizarFlashCardBotaoContinuar(idFlashCard, numeroQuestao, tela, acertos);
        
    }                                              

