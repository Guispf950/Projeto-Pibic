    private void bttnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {  
    	
    	TelaDeMenuAluno tela1 = new TelaDeMenuAluno();
    	String nomeAluno = lblAutor.getText();
    	System.out.println("NOME AUTOR: "+lblAutor.getText());
    	Date aux = new Date();
		Timestamp tempoFinal = new Timestamp(aux.getTime());
		acertos = Integer.valueOf(jLabel3.getText());
    	new PontuacaoController().salvarPontuacao(idFlashCard, nomeAluno, acertos, tempoInicial, tempoFinal);
    	tela1.getjLabel12().setText(tela.getLblAutor().getText());
    	tela1.setVisible(true);
       tela.dispose();
    }                                             

